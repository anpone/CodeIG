if ( ! function_exists('heading'))
{
	/**
	 * Heading
	 * 标题 
	 * Generates an HTML heading tag.
	 *
	 * @param	string	content 内容
	 * @param	int	heading level 标题等级1-6
	 * @param	string
	 * @return	string
	 */
	function heading($data = '', $h = '1', $attributes = '')
	{
		return '<h'.$h._stringify_attributes($attributes).'>'.$data.'</h'.$h.'>';
	}
}

