if ( ! function_exists('_list'))
{
	/**
	 * Generates the list
	 * 生成的列表
	 * Generates an HTML ordered list from an single or multi-dimensional array.
	 * 生成一个HTML从单个或多维数组有序列表。
	 * @param	string
	 * @param	mixed
	 * @param	mixed
	 * @param	int
	 * @return	string
	 */
	function _list($type = 'ul', $list = array(), $attributes = '', $depth = 0)
	{
		// If an array wasn't submitted there's nothing to do... 如果数组不是提交没有什么可做……
		if ( ! is_array($list))
		{
			return $list;
		}

		// Set the indentation based on the depth 基于深度设置缩进
		$out = str_repeat(' ', $depth)
			// Write the opening list tag 写打开列表标记
			.'<'.$type._stringify_attributes($attributes).">\n";


		// Cycle through the list elements.  If an array is 循环遍历列表元素。如果一个数组
		// encountered we will recursively call _list()  遇到我们将递归地调用_list()

		static $_last_list_item = '';
		foreach ($list as $key => $val)
		{
			$_last_list_item = $key;

			$out .= str_repeat(' ', $depth + 2).'<li>';

			if ( ! is_array($val))
			{
				$out .= $val;
			}
			else
			{
				$out .= $_last_list_item."\n"._list($type, $val, '', $depth + 4).str_repeat(' ', $depth + 2);
			}

			$out .= "</li>\n";
		}

		// Set the indentation for the closing tag and apply it 设置关闭标签的缩进和应用它
		return $out.str_repeat(' ', $depth).'</'.$type.">\n";
	}
}

