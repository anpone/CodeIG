if ( ! function_exists('nbs'))
{
	/**
	 * Generates non-breaking space entities based on number supplied
	 * 生成不间断空格实体基于提供的数量
	 * @deprecated	3.0.0	Use str_repeat() instead 使用函数str_repeat()重复插入
	 * @param	int
	 * @return	string
	 */
	function nbs($num = 1)
	{
		return str_repeat('&nbsp;', $num);
	}
