if ( ! function_exists('br'))
{
	/**
	 * Generates HTML BR tags based on number supplied
	 * 生成HTML BR(换行符)标签根据提供的数量
	 * @deprecated	3.0.0	Use str_repeat() instead 使用函数str_repeat()重复插入
	 * @param	int	$count	Number of times to repeat the tag 多次重复标记
	 * @return	string
	 */
	function br($count = 1)
	{
		return str_repeat('<br />', $count);
	}
}

